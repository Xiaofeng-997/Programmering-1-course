#Uppgift: Skapa en lista
list_fruits = ['apple' , 'banana' , 'cherry' , 'kiwi' , 'melon']#här skapde jag ett list med flera string

#Uppgift: Skriv ut det första och sista elementet i listan
print(list_fruits[0], list_fruits[2])

#Uppgift: Ändra elementet i listan
list_fruits[2] = 'strawberry'#här ändra jag det sista elementet
print(list_fruits)

#Uppgift: Lägg till och ta bort element i listan
list_fruits.append('orange')#här lägger jag till ett nytt element sist i listan
print(list_fruits)
list_fruits.remove('banana')#man måste skriva den element som man vill ta bort
print(list_fruits)#det går inte ta bort elementen genom index nummer


#Uppgift: Sortering och Reversering i listan
sorted_list_fruits = sorted(list_fruits)#sorted() ger en ny lista utan att ändra den ursprungliga
print(list_fruits)
list_fruits.sort()#sort() ändrar i listan
print(list_fruits)
list_fruits.sort(reverse=True)#här reverserar jag listan
print(list_fruits)

